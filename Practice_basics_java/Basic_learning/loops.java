package Practice_basics_java.Basic_learning;

public class loops {
    public static void main(String [] args){
    //for loops 
    for(int num=1; num <=5;num++){
        System.out.println(num);
    }
    
    //while loop

    int numm = 1;
    while (numm <= 5){
        System.out.println(numm);
    }
    numm ++;

    //do-while loop
    int n=1;
    do{
        System.out.println(n);
    }while(n<=5);
}
}
